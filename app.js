// ===== 時刻表取得 =====
function getSchedule(stopName, route, direction, mode) {
  const routeData = TIMETABLE[route];
  if (!routeData || !routeData[stopName]) return [];
  const stopData = routeData[stopName];
  if (!stopData[direction] || stopData[direction] === null) return [];
  let schedule;
  if (mode === '18') {
    const fullSchedule = stopData[direction]['19'] || [];
    schedule = fullSchedule.filter(time => {
      const hour = parseInt(time.split(':')[0]);
      return hour <= 20;
    });
  } else {
    schedule = stopData[direction][mode] || [];
  }
  // 8:30以降オープンの場合、6時台の最初3本を運休として除外
  if (state.after830open) {
    let removed = 0;
    schedule = schedule.filter(time => {
      const hour = parseInt(time.split(':')[0]);
      if (hour === 6 && removed < 3) {
        removed++;
        return false;
      }
      return true;
    });
  }
  return schedule;
}

function getNextBuses(stopName, route, mode, direction, count = 3) {
  const schedule = getSchedule(stopName, route, direction, mode);
  const nowSec = nowSeconds();
  const buses = [];
  for (const timeStr of schedule) {
    const [h, m] = timeStr.split(':').map(Number);
    const busSec = h * 3600 + m * 60;
    const diff = busSec - nowSec;
    if (diff >= -60) {
      buses.push({ time: timeStr, diffSeconds: diff, direction });
    }
    if (buses.length >= count) break;
  }
  return buses;
}

